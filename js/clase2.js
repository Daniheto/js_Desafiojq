let color = prompt("ingresar color").toUpperCase();

if (color == "ROJO") {
  alert("LA BANDERA DE JAPON TIENE " + color + " EN EL MEDIO");
} else if (color == "VERDE") {
  alert("EL PRIMER AUTO DE MI PAPÁ ERA " + color);
} else if (color == "AZUL") {
  alert("EL CIELO ES " + color);
} else if (color == "NEGRO") {
  alert("EL " + color + " ES LA PERCEPCIÓN VISUAL DE MAXIMA OSCURIDAD");
} else {
  alert("EL " + color + " ES UN GRAN COLOR");
}
